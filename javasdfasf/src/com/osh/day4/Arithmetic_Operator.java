package com.osh.day4;

public class Arithmetic_Operator {

	public static void main(String[] args) {
		// =
		int i = 20;
		System.out.println(i);
		// +
		i = i + 10; // i += 10;
		System.out.println(i);
		// *
		i = i * 10;
		System.out.println(i);
		// /
		i = i / 3;
		System.out.println(i);
		// %
		System.out.println(i % 2);
		System.out.println(i % 3);
	}

}
