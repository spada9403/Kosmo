package com.osh.day4;

public class Relational_Operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);

		System.out.println(0 == b);
		System.out.println(-1 == a);

		boolean b1 = (a > b);
		System.out.println(b1);
	}

}
